console.log("---- for loop ----");
for( var i = 1; i <= 10; i++ ){	// i++ and ++i is same
	console.log( i );
}

console.log("---- while loop ----");
var j = 1;
while( j <= 10 ){
	console.log( j )
	j++;
}

console.log("---- do - while loop ----");
var k = 11;
do{
	console.log( k )
	k++;
}while( k <= 10 );